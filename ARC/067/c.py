def factorization(n):
    if n <= 1:
        return []

    ret = []
    while n > 2 and n % 2 == 0:
        ret.append(2)
        n //= 2
    i = 3
    while i <= math.sqrt(n):
        if n % i == 0:
            ret.append(i)
            n //= i
        else:
            i += 2
    ret.append(n)
    return ret

def main():
    N = I()
    v = defaultdict(int)

    for i in range(1, N+1):
        fac = factorization(i)
        for f in fac:
            v[f] += 1

    ans = 1
    for c in v.values():
        ans *= (c + 1)
        ans %= MOD
    print(ans)


